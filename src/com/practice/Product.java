package com.practice;

public class Product {
	
	private String productName;
	private int productId;
	private int productPrice;
	private String productIExpirationDate;
	
	

	public String getProductName() {
		return productName;
	}


	public void setProductName(String productName) {
		this.productName = productName;
	}


	public int getProductId() {
		return productId;
	}

	public void setProductId(int productId) {
		this.productId = productId;
	}


	public int getProductPrice() {
		return productPrice;
	}


	public void setProductPrice(int productPrice) {
		this.productPrice = productPrice;
	}

	public String getProductIExpirationDate() {
		return productIExpirationDate;
	}

	public void setProductIExpirationDate(String productIExpirationDate) {
		this.productIExpirationDate = productIExpirationDate;
	}


}
